-- Number Functions 

-- abs() : Returns the absolute value 

select abs(-12);

-- sqrt(n) : Returns the sqrt value 

select sqrt(49);

-- power(n,m) : Returns n power m value 

select power(2,3);

-- ceiling() : Returns the greatest integer value 

select ceiling(12.000001);

-- floor() : Returns the smallest integer value 

select floor(12.9999999999999999);

-- round() : Used to round the value to particular decimals 

select round(9867.8876,3);

select round(9867.8876,2);

select round(9867.8876,1);

select round(9867.8876,0);

select round(9867.8876,-1);

select round(9867.8876,-2);

select round(9867.8876,-3);

